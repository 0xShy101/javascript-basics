// an object is like an object in real life, think of a person, a person has must have a name and age for it to be a person EXAMPLE

let person = {
    name: 'shy',
    age: 19,
};

/* 
note that inside objects we end with (,) instead of (;)
and use (:) instead of (=)
*/

console.log(person); // console shows { name: 'shy', age: 19 }

// let's say we want to change the name inside the object, we will use Dot Notation

person.name = 'theshy101';

// this also works on console.log

console.log(person.name); // console shows theshy101